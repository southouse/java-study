package com.marul.java.leetcode.addtwonumbers;

/**
 * @author iffybot
 */
public class ListNode {
    int value;
    ListNode next;

    ListNode(int value) {
        this.value = value;
    }

    @Override
    public String toString() {
        return "ListNode{" +
                "value=" + value +
                ", next=" + next +
                '}';
    }
}
